--SELF JOIN

SELECT  e1.ENAME as Employee_Name,
		e1.empno as Employee_No ,
		e1.sal as Employee_Sal, 
		e2.managerid as manager_id,
		e2.sal  as manager_sal
FROM  EMP e1  JOIN EMP e2 
ON e1.managerid = e2.empno  AND e1.sal > e2.sal ;



